/**
 * Fixed FX rates used by the demo dataset. Values are expressed as
 * "1 unit of CURRENCY = N USD" (i.e. multiply a CURRENCY amount by the
 * rate below to get USD).
 *
 * These are deliberately hard-coded — the demo must render identically
 * offline and must never hit a live FX API. W4 may override if needed.
 */
export const DEMO_FX_TO_USD: Record<string, number> = {
  USD: 1,
  CNY: 1 / 7.2, // ~0.1389
  HKD: 1 / 7.8, // ~0.1282
  JPY: 1 / 150, // ~0.00667
  EUR: 1 / 0.92, // ~1.0870  (1 EUR ≈ 1.087 USD)
};

/** Number of daily snapshots generated by default. */
const DEFAULT_DAYS = 90;

/** Seed string for the PRNG. Changing this reshuffles every demo curve. */
const DEMO_SEED = "kerdos-demo-v1";

/** Reporting base currency for snapshot totals. */
const BASE_CURRENCY = "USD";

/**
 * Asset types that store their own price on the Asset record rather than
 * deriving it from a ticker feed. Kept local to this module to avoid a
 * cross-module import from snapshotService (data layer stays standalone).
 */
const MANUAL_PRICE_TYPES = new Set<AssetType>([
  "real_estate",
  "vehicle",
  "cash",
  "other",
]);

// ---------------------------------------------------------------------------
// Deterministic PRNG (xmur3 + mulberry32)
// ---------------------------------------------------------------------------
//
// xmur3 hashes a string into a 32-bit seed; mulberry32 turns that seed into
// a uniform [0,1) generator. Together they give us ~10 lines of code, no
// dependencies, and fully reproducible randomness — ideal for a demo chart.
//
// Reference: https://stackoverflow.com/a/47593316

function xmur3(str: string): () => number {
  let h = 1779033703 ^ str.length;
  for (let i = 0; i < str.length; i++) {
    h = Math.imul(h ^ str.charCodeAt(i), 3432918353);
    h = (h << 13) | (h >>> 19);
  }
  return function () {
    h = Math.imul(h ^ (h >>> 16), 2246822507);
    h = Math.imul(h ^ (h >>> 13), 3266489909);
    h ^= h >>> 16;
    return h >>> 0;
  };
}

function mulberry32(seed: number): () => number {
  let a = seed >>> 0;
  return function () {
    a = (a + 0x6d2b79f5) >>> 0;
    let t = a;
    t = Math.imul(t ^ (t >>> 15), t | 1);
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

/** Build a uniform [0,1) generator from a composite seed. */
function makeRng(...parts: (string | number)[]): () => number {
  const key = [DEMO_SEED, ...parts].join("|");
  const seedFn = xmur3(key);
  return mulberry32(seedFn());
}

/**
 * Standard-normal sample via Box-Muller, consuming 2 uniforms per call.
 * Clamps u1 away from zero to avoid log(0) = -Infinity.
 */
function gaussian(rng: () => number): number {
  const u1 = Math.max(rng(), 1e-9);
  const u2 = rng();
  return Math.sqrt(-2 * Math.log(u1)) * Math.cos(2 * Math.PI * u2);
}

// ---------------------------------------------------------------------------
// Date helpers
// ---------------------------------------------------------------------------

function toYMD(d: Date): string {
  const y = d.getFullYear();
  const m = String(d.getMonth() + 1).padStart(2, "0");
  const day = String(d.getDate()).padStart(2, "0");
  return `${y}-${m}-${day}`;
}

function toCompact(d: Date): string {
  const y = d.getFullYear();
  const m = String(d.getMonth() + 1).padStart(2, "0");
  const day = String(d.getDate()).padStart(2, "0");
  return `${y}${m}${day}`;
}

/** Return a new Date shifted by N whole days (UTC-agnostic, local time). */
function shiftDays(base: Date, offsetDays: number): Date {
  const d = new Date(base);
  d.setHours(0, 0, 0, 0);
  d.setDate(d.getDate() + offsetDays);
  return d;
}

function toUSD(amount: number, currency: string): number {
  const rate = DEMO_FX_TO_USD[currency];
  if (rate === undefined) {
    // Unknown currency -> pass through. Should not happen for demo data.
    return amount;
  }
  return amount * rate;
}

