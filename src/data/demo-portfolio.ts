/**
 * Build 20 demo Asset records. All rows are tagged `isDemo: true` and share
 * the same creation timestamp (the start of the 90-day window) so the demo
 * chart includes them from day 0.
 */
export function generateDemoAssets(): Asset[] {
  const start = shiftDays(new Date(), -(DEFAULT_DAYS - 1));
  const ts = start; // same Date instance for createdAt/updatedAt

  return ASSET_SPECS.map((spec): Asset => {
    const base: Asset = {
      id: spec.id,
      name: spec.name,
      type: spec.type,
      quantity: spec.quantity,
      costCurrency: spec.costCurrency,
      createdAt: ts,
      updatedAt: ts,
      isDemo: true,
    };
    if (spec.ticker !== undefined) base.ticker = spec.ticker;
    if (spec.costPrice !== undefined) base.costPrice = spec.costPrice;
    if (spec.manualPrice !== undefined) {
      base.manualPrice = spec.manualPrice;
      base.manualPriceUpdatedAt = ts;
    }
    if (spec.notes !== undefined) base.notes = spec.notes;
    if (spec.subType !== undefined) base.subType = spec.subType;
    if (spec.region !== undefined) base.region = spec.region;
    return base;
  });
}

/**
 * Generate `days` daily end-of-day snapshots. Prices walk forward from each
 * asset's `startPrice` using a seeded geometric random walk; two scripted
 * market events are layered on top. Values are summed into USD using the
 * fixed FX table above.
 */
export function generateDemoSnapshots(
  assets: Asset[],
  days: number = DEFAULT_DAYS,
): Snapshot[] {
  if (days <= 0) return [];

  // Build a quick lookup from assetId → spec so we can replay the price walk.
  const specById = new Map<string, AssetSpec>();
  for (const s of ASSET_SPECS) specById.set(s.id, s);

  // Precompute the per-day unit price for every asset. We evolve prices
  // with a log-return random walk so they can't go negative.
  //   price[d] = price[d-1] * exp(drift + vol*gaussian + event_shock)
  // Manual-price assets (real estate / cash) still evolve so the demo has
  // believable total-networth movement, but with tiny vol.
  const pricesByAsset: Record<string, number[]> = {};
  for (const spec of ASSET_SPECS) {
    const rng = makeRng("price", spec.id);
    const series: number[] = new Array(days);
    let p = spec.startPrice;
    series[0] = p;
    for (let d = 1; d < days; d++) {
      const shock = gaussian(rng);
      const ev = eventShock(spec.type, d);
      const logRet = spec.drift + spec.vol * shock + ev;
      p = p * Math.exp(logRet);
      series[d] = p;
    }
    pricesByAsset[spec.id] = series;
  }

  // FX noise: for cash assets we let the effective USD value wobble slightly
  // via a per-currency, per-day multiplier. Hard-coded currencies stay
  // deterministic by seeding on the currency code.
  const fxWalk: Record<string, number[]> = {};
  for (const cur of Object.keys(DEMO_FX_TO_USD)) {
    const rng = makeRng("fx", cur);
    const arr: number[] = new Array(days);
    let m = 1;
    arr[0] = m;
    for (let d = 1; d < days; d++) {
      const sigma = cur === "USD" ? 0 : 0.003;
      m = m * Math.exp(sigma * gaussian(rng));
      arr[d] = m;
    }
    fxWalk[cur] = arr;
  }

  // Only include assets we actually have specs for. (Defensive — caller
  // normally passes the output of generateDemoAssets.)
  const activeAssets = assets.filter((a) => specById.has(a.id));

  const today = new Date();
  today.setHours(0, 0, 0, 0);
  const snapshots: Snapshot[] = new Array(days);

  for (let d = 0; d < days; d++) {
    const date = shiftDays(today, -(days - 1 - d));
    const dateStr = toYMD(date);
    const breakdown: SnapshotBreakdown[] = [];
    let total = 0;

    for (const asset of activeAssets) {
      const spec = specById.get(asset.id)!;
      const isManual = MANUAL_PRICE_TYPES.has(spec.type);

      // Unit price in the asset's quoted currency.
      let unitPrice: number;
      if (isManual && spec.manualPrice !== undefined) {
        // Manual-price assets (real estate / cash / other) use the walked
        // series anchored at manualPrice, so real estate still appreciates
        // slowly and cash stays at ~1.
        const walked = pricesByAsset[spec.id][d];
        unitPrice = walked;
      } else {
        unitPrice = pricesByAsset[spec.id][d];
      }

      const rawValue = unitPrice * asset.quantity;

      // Convert quoted currency → USD, adding a tiny FX wobble per day.
      const fxMult = fxWalk[spec.priceCurrency]?.[d] ?? 1;
      const valueUSD = toUSD(rawValue, spec.priceCurrency) * fxMult;

      breakdown.push({
        assetId: asset.id,
        value: valueUSD,
        price: unitPrice,
      });
      total += valueUSD;
    }

    snapshots[d] = {
      id: `demo-snap-${toCompact(date)}`,
      date: dateStr,
      totalNetWorth: total,
      currency: BASE_CURRENCY,
      breakdown,
      createdAt: new Date(date.getFullYear(), date.getMonth(), date.getDate()),
      intraday: false,
    };
  }

  return snapshots;
}

/**
 * One-shot helper for W4: returns the 20 demo assets plus `days` daily
 * snapshots, both ready for `db.assets.bulkPut` / `db.snapshots.bulkPut`.
 */
export function buildDemoPortfolio(days: number = DEFAULT_DAYS): {
  assets: Asset[];
  snapshots: Snapshot[];
} {
  const assets = generateDemoAssets();
  const snapshots = generateDemoSnapshots(assets, days);
  return { assets, snapshots };
}
