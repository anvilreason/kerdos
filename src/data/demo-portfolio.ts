/**
 * Deterministic demo portfolio for Kerdos.
 *
 * All records carry `isDemo: true` so the Demo Mode exit path (W4) can wipe
 * them in a single indexed query without touching real user data.
 *
 * Price evolution is driven by a seeded PRNG (xmur3 + mulberry32), so
 * `buildDemoPortfolio()` returns byte-identical output on every call.
 * This keeps the landing chart stable across visits and makes W4 visual
 * regression testing trivial.
 *
 * IMPORTANT: This module is DATA-ONLY. It does not touch Dexie, React,
 * or any network / price service. W4 is responsible for `db.assets.bulkPut`
 * and `db.snapshots.bulkPut`.
 */

