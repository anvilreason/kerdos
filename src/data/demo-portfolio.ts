// ---------------------------------------------------------------------------
// Market events
// ---------------------------------------------------------------------------
//
// Two scripted events give the 90-day curve a "story": a mid-period pullback
// and a late-period rebound. Each event injects a fixed daily shock on top
// of the PRNG walk for the affected asset classes.

interface EventWindow {
  startDay: number; // inclusive
  endDay: number; // inclusive
  /** Daily shock per type bucket (applied to log-return additively). */
  shockByBucket: Partial<Record<"stock" | "crypto" | "etf" | "gold", number>>;
}

const EVENTS: EventWindow[] = [
  // Pullback: days 40..45 (6 trading days)
  // Stocks -5% to -8% total → per-day ≈ -1.1%  (6 days × -1.1% ≈ -6.4%)
  // Crypto -15% total       → per-day ≈ -2.7%
  {
    startDay: 40,
    endDay: 45,
    shockByBucket: {
      stock: -0.011,
      etf: -0.011,
      crypto: -0.027,
      gold: 0.004, // mild safe-haven bid
    },
  },
  // Rebound: days 75..78 (4 trading days)
  // Stocks +4% total → per-day ≈ +1.0%
  // Crypto +10% total → per-day ≈ +2.4%
  {
    startDay: 75,
    endDay: 78,
    shockByBucket: {
      stock: 0.01,
      etf: 0.01,
      crypto: 0.024,
    },
  },
];

function bucketOf(
  type: AssetType,
): "stock" | "crypto" | "etf" | "gold" | "other" {
  if (type === "us_stock" || type === "cn_stock") return "stock";
  if (type === "etf") return "etf";
  if (type === "crypto") return "crypto";
  if (type === "gold") return "gold";
  return "other";
}

function eventShock(type: AssetType, day: number): number {
  const b = bucketOf(type);
  let total = 0;
  for (const ev of EVENTS) {
    if (day < ev.startDay || day > ev.endDay) continue;
    if (b === "other") continue;
    const s = ev.shockByBucket[b];
    if (s !== undefined) total += s;
  }
  return total;
}

